     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $nombre=$request->nombre;
        $email=$request->email;
        $password=$request->password;
        $usuario= User::findorfail($id);
        $usuario->name=$nombre;
        $usuario->email=$email;
        $usuario->password=$password;
        $usuario->save();
        return response()->json(true);
    }

